package com.core.walletservice.exceptions;

public class InsufficientFundsException extends Exception {

    // Constructors
    public InsufficientFundsException(String message) {
        super(message);
    }

    public InsufficientFundsException(String message, Throwable cause) {
        super(message, cause);
    }

    public InsufficientFundsException(Throwable cause) {
        super(cause);
    }

    // This constructor is useful for exceptions that are thrown by methods that do not throw checked exceptions
    public InsufficientFundsException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
